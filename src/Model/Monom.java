package Model;

public class Monom {
    double c,p;
    public Monom(double c, double p) {
        this.c = c;
        this.p = p;
    }

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }

    public double getP() {
        return p;
    }

    public void setP(double p) {
        this.p = p;
    }
}
